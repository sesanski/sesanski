        private void переносПоСловамToolStripMenuItem_Click(object sender, EventArgs e)
        {
            // ПереносПоСловам();
            //Перенос по столбам
            if (richTextBox1.WordWrap != true)
            {
                richTextBox1.WordWrap = true;
                переносПоСловамToolStripMenuItem.Checked = true;
            }
            else
            {
                richTextBox1.WordWrap = false;
                переносПоСловамToolStripMenuItem.Checked = false;
            }

            ToolStripLabel linesLabel = new ToolStripLabel(richTextBox1.Lines.Length.ToString());
            ToolStripLabel charactersLabel = new ToolStripLabel(richTextBox1.TextLength.ToString());
            linesLabel.Text = "Строк: " + (richTextBox1.GetLineFromCharIndex(richTextBox1.TextLength) + 1).ToString();
            charactersLabel.Text = "Символов: " + richTextBox1.Text.Length;
            statusStrip1.Items.Clear();
            statusStrip1.Items.Add(linesLabel);
            statusStrip1.Items.Add(charactersLabel);
        }

        private void richTextBox1_SelectionChanged_1(object sender, EventArgs e)
        {
            отменитьToolStripMenuItem.Enabled = true;
            if (richTextBox1.SelectedText != "")
            {
                вырезатьToolStripMenuItem.Enabled = true;
                копироватьToolStripMenuItem.Enabled = true;
                удалитьToolStripMenuItem.Enabled = true;
            }
            else
            {
                вырезатьToolStripMenuItem.Enabled = false;
                копироватьToolStripMenuItem.Enabled = false;
                удалитьToolStripMenuItem.Enabled = false;
            }
        }

        private void richTextBox1_Leave_1(object sender, EventArgs e)
        {
            вырезатьToolStripMenuItem.Enabled = false;
            копироватьToolStripMenuItem.Enabled = false;
            удалитьToolStripMenuItem.Enabled = false;
        }

        private void CheckClipboard()
        {
            if (Clipboard.ContainsText())
            {
                вставитьToolStripMenuItem.Enabled = true;
            }
            else
            {
                вставитьToolStripMenuItem.Enabled = false;
            }
        }

